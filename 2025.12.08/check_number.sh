#!/bin/bash
# Скрипт проверки знака числа

# Запрашиваем число у пользователя
read -p "Введите число: " number

# Проверка на ноль
# -eq означает "равно" (equal)
if [ $number -eq 0 ]; then
    echo "Вы ввели ноль"

# Проверка на положительное число
# -gt означает "больше чем" (greater than)
elif [ $number -gt 0 ]; then
    echo "Вы ввели положительное число"

# Если не ноль и не положительное, значит отрицательное
else
    echo "Вы ввели отрицательное число"
fi