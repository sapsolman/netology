#!/bin/bash
# Скрипт математических операций

echo "Калькулятор тук-тук"
echo "==========="

# Вводим первое число
read -p "Введите первое число: " num1

# Вводим второе число
read -p "Введите второе число: " num2

# Выводим меню доступных операций
echo ""
echo "Выберите операцию:"
echo "1. Сложение (+)"
echo "2. Вычитание (-)"
echo "3. Умножение (*)"
echo "4. Деление (/)"
echo ""

# Выбор операции
read -p "Ваш выбор (1-4): " choice

# case для обработки выбора операции
case $choice in
    1)
        # СЛОЖЕНИЕ: используем арифметическую операцию $((...))
        result=$((num1 + num2))
        echo "Результат сложения: $num1 + $num2 = $result"
        ;;
    
    2)
        # ВЫЧИТАНИЕ
        result=$((num1 - num2))
        echo "Результат вычитания: $num1 - $num2 = $result"
        ;;
    
    3)
        # УМНОЖЕНИЕ
        result=$((num1 * num2))
        echo "Результат умножения: $num1 * $num2 = $result"
        ;;
    
    4)
        # ДЕЛЕНИЕ: проверяем деление на ноль
        if [ $num2 -eq 0 ]; then
            echo "Ошибка: деление на ноль невозможно!"
        else
            # Для деления используем bc для точности
            result=$(echo "scale=2; $num1 / $num2" | bc)
            echo "Результат деления: $num1 / $num2 = $result"
        fi
        ;;
    
    *)
        # Обработка неверного выбора
        echo "Неверный выбор! Пожалуйста, выберите число от 1 до 4."
        ;;
esac

echo ""
echo "Операция завершена."