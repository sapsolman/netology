#!/bin/bash
# Менеджер файлов для создания и удаления

# Создаём директорию
# -p каскадное создание, при необходимости
mkdir -p my_test_dir

# Переход в созданную директорию
cd my_test_dir

echo "Создаем файлы..."

# Создаётся 3 файла с разными именами
touch file1.txt file2.txt file3.txt

# Показываем созданные файлы
echo "Фиксируем расположение:"
pwd

echo "Созданные файлы:"
ls -lah

echo "Удаляем файлы..."

# Удаляем все созданные файлы
# -f без лищних вопросов
rm -f file1.txt file2.txt file3.txt

# Выход из директории
cd ..

# Удаляет пустую директорию
rmdir my_test_dir

echo "Операции завершены!"