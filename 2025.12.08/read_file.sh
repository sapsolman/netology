#!/bin/bash
# Скрипт чтения файла построчно

# Проверяем, передан ли аргумент с именем файла и пути к нему
if [ $# -eq 0 ]; then
    echo "Использование: $0 <имя_файла>"
    exit 1
fi

# Передаём имя файла в переменную
filename=$1

# Проверяем существование файла по переданному пути
if [ ! -f "$filename" ]; then
    echo "Файл $filename не существует!"
    exit 1
fi

echo "Содержимое файла $filename:"

# Читаем файл построчно
# while читает каждую строку и сохраняет в переменную line
# read -r отключает интерпретацию обратного слеша
while IFS= read -r line; do
    # Выводим текущую строку
    echo "$line"
done < "$filename"

echo "Чтение завершено!"