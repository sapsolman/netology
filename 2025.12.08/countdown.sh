#!/bin/bash
# Скрипт обратного отсчета

# Получаем аргумент
# $# содержит количество переданных аргументов
if [ $# -eq 0 ]; then
    echo "Использование: $0 <число>"
    exit 1
fi

# $1 - первый переданный аргумент
start=$1

# Цикл for для обратного отсчета
# seq генерирует последовательность чисел
# start -1 0 означает от $start до 0 с шагом -1
for i in $(seq $start -1 0); do
    # Выводим текущее число
    echo $i
    # Пауза 1 секунда между числами
    sleep 1
done

echo "Обратный отсчет завершен!"